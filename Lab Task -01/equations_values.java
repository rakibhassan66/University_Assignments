public class equations_values {
    public static void main(String[] args) {
        int A = 5;
        int B = 6;
        int C = 7;
        int D = 8;

        int resultA = A * B - C * D;
        int resultB = 2 * A - B + 3 * D;
        int resultC = A * A + B * B - C * C + D * D;
        int resultD = A * A * A + B - C;

        System.out.println("a) Result: " + resultA);
        System.out.println("b) Result: " + resultB);
        System.out.println("c) Result: " + resultC);
        System.out.println("d) Result: " + resultD);
    }
}

